                        <p class="text-white-50 text-left"> • Gunung yang merupakan puncak tertinggi di pulau Kalimantan untuk wilayah Indonesia.<br>
                                    • Berada di perbatasan dua provinsi yaitu Kalimantan Barat dan Kalimantan Tengah.<br>
                                    • Gunung yang letaknya sangat terpencil sehingga membutuhkan perjalanan beberapa hari untuk sampai di titik awal pendakiannya.<br>
                                    • Letaknya yang terpencil membuat biaya untuk mendaki gunung ini menjadi mahal dan dalam jajaran konsep tujuh puncak Indonesia gunung ini menduduki urutan kedua sebagai gunung yang mahal biaya pendakiannya setelah Carstenzs di Papua.<br>
                                    • Gunung Bukit Raya bukan gunung berapi, akan tetapi merupakan bagian dari sebuah rangkaian pegunungan. <br>
                                    • Termasuk pada jajaran pegunungan Muller Schwaner.<br>
                                    • Merupakan bagian dari Taman Nasional Bukit Baka Bukit Raya, berdasarkan SK Menteri Kehutanan no. 281/Kpts-II/1992 dengan luas 181.090 hektar.<br>
